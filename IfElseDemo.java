public class IfElseDemo{
    public static void main(String args[]){
        int x=20;
        if(x>10){
            System.out.println("the given number x is greater than 10");
        }
        else if(x>10){
            System.out.println("the given number x is less than 10");
        }
        else{
            System.out.println("the given number is equal");
        }
        System.out.println("final destination");

        }
 }
